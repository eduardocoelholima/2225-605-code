package classes.basics;

class StringAndInteger2
{
   public static void main(String args[])
   {
      System.out.println(3 + 7);
      System.out.println(3 + 7 + "abc");
      System.out.println(3 + 7 + "abc" + 1);
      System.out.println(3 + 7 + "abc" + 1 + 2);
      System.out.println("" + 3 + 7 + "abc" + 1 + 2);
      System.out.println("" + ( 3 + 7 ) + "abc" + ( 1 + 2 ));
   }
}

